    <!-- Intro Content -->
    <div class="row">
      <div class="col-lg-12" style="text-align: justify;">
        <p>O Laboratório Virtual de Educação Matemática (LAVEM) do Instituto Federal de Educação, Ciência e Tecnologia do Sul de Minas Gerais - IFSULDEMINAS Campus Passos tem como objetivos: contribuir com a formação inicial dos alunos do curso de Licenciatura em Matemática quanto à inserção ao universo da pesquisa e aprimorar as práticas pedagógicas possibilitando aos professores em exercício na Educação Básica a ampliação de seus saberes docentes através de um ambiente virtual que disponibilize objetos de aprendizagem voltados para a resolução de problemas e a utilização das novas tecnologias da informação e comunicação.
        </p>
        <p>Para os alunos da Educação Básica o LAVEM disponibiliza um acervo de questões: questões de vestibulares, Exame Nacional do Ensino Médio (ENEM) e Olimpíada Brasileira de Matemática da Escola Pública (OBMEP). Esse acervo possibilita a consolidação de conceitos aprendidos em sala de aula e através de objetos de aprendizagem, também disponibilizados no site, poderá o aluno resgatar conceitos matemáticos em defasagem. O LAVEM pode ser considerado uma ampliação do Laboratório de Ensino de Matemática (LEM) do IFSULDEMINAS Campus Passos, configurando-se como subsídio para cursos de ensino e extensão, possibilitando a articulação entre teoria e prática curricular: um dos objetivos do Curso de Licenciatura de Matemática.
        </p>
