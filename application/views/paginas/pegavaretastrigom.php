<p>2. Após se organizarem em duplas será realizado um sorteio para definição da dupla que começara à partida.
</p>


<p>3. Seguindo as regras do jogo, inicia-se a partida no sentido horário, pelo participante à esquerda do jogador que soltou as varetas no centro da mesa.</p>



<p>4. Na sua vez, o jogador tenta retirar uma vareta da cor que lhe parece mais conveniente. Se ele retirou uma vareta vermelha, por exemplo, registra nas suas anotações pessoais a cor e o valor correspondente a vareta conquistada e volta a retirar uma nova vareta enquanto consiga efetuar a retirada de maneira correta. O jogador passará a vez se ao retirar uma vareta e as demais mexerem.
</p>


<p>5. No final da partida, cada jogador fará os cálculos relacionados às varetas obtidas em π radianos para efetuar a somatória final da dupla. Os resultados das duplas serão convertidos de π radianos para graus.
</p>


<p>6. Ganhará o jogo a dupla cuja somatória resultar um número maior de voltas no ciclo trigonométrico.</p>

<h4 class="card-title"><b>Algumas problematizações</b></h4>

<p>O jogo "Pega varetas trigonométrico" como estratégia de ensino para trigonometria permite problematizações. Seguem algumas possibilidades:</p>
<p>(1) Representar a pontuação conquistada por meio de expressões numéricas envolvendo as operações fundamentais.
</p>
<p>(2) Organizar os resultados obtidos de cada dupla em um gráfico de barras, para saber qual dupla obteve maior pontuação no jogo. Lembre-se que π=3,14.
</p>
<p>(3) Organizar os resultados obtidos de cada dupla realizando o comparativo com as demais duplas para analisar qual deu volta no círculo trigonométrico.
</p>
<p>(4) Pensar em situações como: A dupla A conquistou em sua partida do “Pega varetas trigonométrico” 2385°, enquanto a dupla B somou 1620°. Quantas varetas a mais e em que cores a dupla B deveria ter conquistado para ficar em primeiro lugar?
</p>
<p>(4.1) Para resolver o problema da dupla B existem outras formas de combinar as varetas necessárias? Se sim, cite algumas.</p>
