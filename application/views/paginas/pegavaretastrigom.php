	<h4 class="card-title"><b>Regras do jogo</b></h4>
	<p>O jogo pega varetas trigonométrico é composto pelo jogo pega varetas com adaptações quanto aos valores de suas varetas,
	conforme especificado a seguir:</p>
	<p>- Preta: 2π;
	- Vermelha:  π/2;</br>
- Amarela: π/3;</br>
- Azul: π/4;</br>
- Verde: π/6.</br></p>
<p>As respectivas duplas de cada grupo iniciam o jogo em sentido horário e o feixe de varetas será lançado sob uma mesa plana. Cada jogador, na sua vez, fará a retirada de uma varetas sem que se mova as demais e caso isso ocorra o jogador perderá sua vez passando ao próximo jogador. Considerando que cada cor de vareta tem um valor em π radiano, vencerá o jogo a dupla que conquistar a maior soma em π radianos e consequentemente der mais voltas no cíclo trigonométrico. Para que os alunos consolidem a transformação das medidas dos ângulos de graus para π radianos, o ciclo trigonométrico terá suas medidas em graus, permitindo assim que os mesmo ao final da partida do jogo efetuem os cálculos para as conversões e definição do vencedor.
</p>

<h4 class="card-title"><b>Orientação aos alunos quanto às regras</b></h4>

<p>1. Cada jogador colocará seu nome nas folhas com ciclo trigonométrico impresso e a tabela para anotações. As medidas dos ângulos serão em graus.</p>
