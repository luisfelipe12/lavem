  <!-- Intro Content -->
  <div class="row">
   <div class="col-lg-12" style="text-align: justify;">
    <p>A Resolução de Problemas caracteriza-se quanto ao seu papel no
      ensino de Matemática, segundo Diniz e Smole (2001), por meio de cinco
    concepções que não se excluem e sim se complementam. São elas: </p>
    <p><b>(a)Resolução de Problemas como meta:</b> de acordo com Diniz e Smole
      (2001), ela é compreendida de forma simplista, como sendo o único objetivo do
      ensino de matemática, ou seja, todo o processo de ensino da disciplina em
      questão busca exclusivamente levar o aluno a resolver problemas. Os
      currículos são voltados ao desenvolvimento no aluno de condições adequadas
      para que ele consiga ter acesso às informações e conceitos necessários e
    assim poder enfrentar e resolver problemas.</p>
    <p><b>(b)Resolução de Problemas como processo:</b> neste caso, segundo Diniz e
      Smole (2001) um processo de aplicação onde o solucionador deverá aplicar
      seus conhecimentos prévios em situações novas. As implicações deste
      entendimento sobre a Resolução de Problemas no ensino da Matemática
      enfatizam o enfoque dado aos procedimentos ou passos utilizados para chegar
      a uma determinada solução. As respostas dos problemas, até então muito
      consideradas, perderam sua importância. Surgem nesta concepção à
      classificação de tipos de problemas, tipos de estratégias de resolução e
    esquemas norteados por passos a serem seguidos.</p>
    <p><b>(c) Resolução de Problemas como habilidade básica:</b> sugere, segundo as
      autoras, uma competência mínima para que o indivíduo possa ser inserido no
      mundo do conhecimento e do trabalho. No final dos anos 1970 e 1980, as
      indicações de que os alunos precisavam saber resolver problemas trazem
      implicações ao ensino de matemática como maior atenção na escolha de
      técnicas para se resolver um problema e quais os tipos deveriam ser ensinados
    aos alunos.</p>
    <p><b>(d) Resolução de Problemas como metodologia:</b> caracteriza-se pela
      utilização de um problema detonador ou desafiador que pudesse desencadear
      o ensino e a aprendizagem de conhecimentos matemáticos. Trabalhar com
      problemas abertos, problematizações ou na formulação de problemas em
    projetos também se enquadra nesta concepção.</p>
    <p><b>(e) Resolução de problemas como perspectiva metodológica:</b> além de
      considerar como problema toda situação que permita alguma problematização,
      busca, fora propor esta situação-problema, resolvê-la, questionar a própria
      situação inicial proposta e as respostas obtidas. O aluno deve manter uma
      postura de investigação científica em relação àquilo que está pronto. A
      resposta correta perde o seu valor se o processo que levou o solucionador a
      ela não for enfatizado e analisado. A postura do professor, como um sujeito
      que instiga todo o processo da Resolução de Problemas, diferencia-se das
    demais concepções (Diniz e Smole, 2001).</p>
    <p>Ao analisar as concepções acima descritas, notamos que elas não se
      excluem e, por isso, pensamos ser necessário destacar, que neste ambiente
      virtual, devido a sua amplitude, a Resolução de Problemas será tratada como
      uma perspectiva metodológica que se correlaciona com os objetos de
      aprendizagens dinâmicos e estáticos, a fim de promover a aprendizagem
    significativa dos conceitos matemáticos. </p>
