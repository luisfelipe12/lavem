  <p style= "text-align:justify;">
    A mesa redonda da abertura abordou as políticas de formação de professores de Matemática e foi composta 
    pelos professores doutores do IFSULDEMINAS, Willian Cruz-Campus Pouso Alegre, Adriana Correia e Luciana Buranello,
    ambas do Campus Passos. As palestras promoveram discussões sobre a socialização das aplicações da Matemática e os 
    desafios dos acadêmicos e profissionais da área. O professor Richael Silva (Doutorando da Unesp) proferiu a palestra
    intitulada de “Formação Inicial de Professores (de Matemática) e a influência dos saberes da experiência pré profissional:
    dificuldades e possibilidades”. Foram ministradas, ainda, palestras direcionadas à matemática pura pelos professores Jarne
  Donizetti e Tadeu Vilela. A 2ª SEMAT contou, também, com uma Exposição de Materiais Didáticos Manipuláveis que marcou a inauguração do Laboratório de 
   Educação Matemática (LEM) do IFSULDEMINAS Campus Passos. De acordo com o Coordenador do curso de Licenciatura em Matemática
   professor Benjamim José Esteves “este laboratório será um recurso essencial para contribuir para a formação dos futuros professores
 de Matemática.”</p>
  
