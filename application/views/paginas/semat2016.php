      <p style="text-align:justify;">O curso de Licenciatura em Matemática promoveu, entre os dias 14 e 17 de setembro, a 2ª Semana da Matemática – II SEMAT.
        Durante o evento, os profissionais e acadêmicos tiveram a oportunidade de socializar experiências e compartilhar o
        conhecimento por meio de oficina, palestras, minicursos, mesa redonda e exposições.
        Os minicursos e oficinas proporcionaram aos presentes o contato com diversas técnicas, softwares, 
        ferramentas da internet e aplicativos voltados para a Matemática. Um dos minicursos explorou o tema 
        “Abordagem Moderna na Resolução Algébrica e Gráfica de Inequações Produto-Quociente” e foi ministrado pelo 
        professor convidado Marcelo Lemos (IBMEC – MG). Foram oferecidas diversas oficinas ministradas pelos
      professores Fredy Coelho, Luciano Carrijo, Adriana Correia, Luciana Buranelo e Janaína Faustino.</p>
