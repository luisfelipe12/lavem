       <p>Edital PIBID nº. 31/2018 – Bolsista de iniciação à docência. O Diretor do Instituto Federal de Educação, Ciência e Tecnologia do Sul de Minas Gerais – IFSULDEMINAS Campus Passos, no uso de suas atribuições, torna público que estarão abertas as inscrições para o Programa Institucional de Bolsa do Programa de Iniciação à Docência (PIBID). A coordenação de área do PIBID – IFSULDEMINAS, disciplina de matemática, selecionará licenciandos deste instituto para bolsistas de Iniciação à Docência-CAPES. Os vinte e quatro (24) primeiros alunos selecionados receberão uma bolsa no valor de R$ 400,00 (quatrocentos reais) mensais durante o período de 18 meses ou enquanto o aluno permanecer no programa. Os alunos não selecionados dentro das 24 vagas, mas classificados, poderão fazer parte do projeto como Pibidianos voluntários - não bolsistas – e, também deverão firmar Termo de Compromisso, além de estarem, via ordem classificatória, habilitados a receber bolsa caso haja alguma desistência ao longo do período.

       </p>
       <p style="text-align:center"><b>Sobre o PIBID</b></p>
       <p>a) Por definição, a iniciação à docência visa proporcionar aos discentes na primeira metade do curso
        de licenciatura uma aproximação prática com o cotidiano das escolas públicas de educação básica
      e com o contexto em que elas estão inseridas;</br>
      b) Dentre os objetivos, podem ser citados:
    - Incentivar a formação de docentes em nível superior para a educação básica;</br>
  - Contribuir para a valorização do magistério;</br>
- Elevar a qualidade da formação inicial de professores;</br>
- Inserir os licenciandos no cotidiano de escolas da rede pública de educação,
proporcionando-lhes oportunidades de criação e participação em experiências metodológicas,
tecnológicas e práticas docentes de caráter inovador e interdisciplinar que busquem a
superação de problemas identificados no processo de ensino aprendizagem nos cursos de
licenciatura, promovendo a integração entre educação superior e educação básica;</br>
- Incentivar escolas públicas de educação básica, mobilizando seus professores como coformadores
dos futuros docentes e tornando-as protagonistas nos processos de formação
inicial para o magistério;</br>
- Contribuir para a articulação entre teoria e prática necessárias à formação dos docentes,
elevando a qualidade das ações acadêmicas nos cursos de licenciatura.</p>
