	<h4 class="card-title"><b>Anos de aplicação</b></h4>
	<p>Ensino fundamental: 7º, 8º e 9º anos.</p>

	<h4 class="card-title"><b>Regras do jogo</b></h4>
	<p>O jogo pega varetas é composto por varetas coloridas: vermelhas, amarelas, verdes, azuis e preta. A cada cor será atribuído um valor adaptado ao conjunto dos números inteiros fazendo uma analogia entre “ganhar (+)” e “perder (-)”, sendo: (a) Vermelha: ganha 1 ponto; (b) Amarela: perde 3 pontos; (c) Verde: perde 4 pontos; (d) Azul: ganha 2 pontos e (e) Preta: ganha 5 pontos. Os jogadores organizados em duplas ou grupos com quatro participantes devem eleger um único jogador para segurar as varetas e soltá-las sobre o centro de uma mesa. Os participantes irão seguir o sentido anti-horário em relação a esse jogador durante todo o jogo. Feito isso, cada jogador irá retirar, na sua vez, uma das varetas sem mexer as demais. Caso consiga, poderá retirar outra, senão passará a vez da jogada para outro participante, recolocando a vareta no jogo. Pode-se retirar as varetas utilizando outras varetas já retiradas como apoio. Vence o jogo o participante que somar mais pontos em relação ao número de varetas conquistadas.

	</p>


