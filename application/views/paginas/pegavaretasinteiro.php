	<h4 class="card-title"><b>Objetivos do jogo</b></h4>
	<p>Desenvolver nos alunos habilidades cognitivas relacionadas aos números inteiros, comparação entre eles e a resolução de problemas promovendo a consolidação dos conceitos e procedimentos envolvendo as operações fundamentais no campo dos inteiros.
	</p>


	<h4 class="card-title"><b>Organização da classe</b></h4>
	<p>Duplas ou grupos com quatro alunos.
