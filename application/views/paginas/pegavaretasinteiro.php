	<p>2. Após se organizarem em grupo, decide-se quem irá soltar as varetas sobre o centro da mesa.</p>


	<p>3. Seguindo as regras do jogo, inicia-se a partida no sentido anti-horário, pelo
	participante à direita do jogador que soltou as varetas no centro da mesa.</p>



	<p>4. Na sua vez, o jogador tenta retirar uma vareta da cor que lhe parece mais conveniente. Se ele retirou uma vareta vermelha, por exemplo, registra nas suas anotações pessoais a cor e o valor correspondente a vareta conquistada e volta a retirar uma nova vareta enquanto consiga efetuar a retirada de maneira correta. O jogador passará a vez se ao retirar uma vareta e as demais mexerem.
	</p>


	<p>5. No final da partida, cada jogador fará os cálculos relacionados às varetas obtidas em π radianos para a posteriori efetuar a somatória da dupla. Os resultados das duplas serão convertidos de π radianos para graus.
	</p>


	<p>6. No final da partida, os jogadores somarão os pontos registrados nas anotações
	pessoais realizando as comparações necessárias e definindo o(s) ganhador(es).</p>

