    <div class="col-md-4">
      <p style="text-align:justify;">De 14 a 16 de setembro, o IFSULDEMINAS - Campus Passos realizou a terceira edição da Semana da Matemática. Este ano, o evento trouxe a
        temática “Práticas de ensino” em suas palestras, oficinas e minicursos, com o propósito de fomentar reflexões sobre o ensino, a pesquisa e a extensão no campo da
        Matemática Pura, Matemática Aplicada, Educação Matemática e Estatística.
        Na noite de abertura, estiveram reunidos professores da área e estudantes de Matemática e Física do IFULDEMINAS e da Universidade do Estado de Minas Gerais (UEMG – Unidade Passos), 
      além de servidores ligados à educação do município.</p>
    </div>
  </div>
  
  <p style= "text-align:justify;">
    Convidado para palestrar sobre “O início da carreira docente”, o analista educacional da Superintendência Regional de Ensino (SRE), Domingos Soares, explicou como funciona o
    ingresso do docente nos cargos do estado, abordando questões como processos seletivos, remuneração do profissional, capacitações e aposentadoria. “É importante que vocês 
    conheçam o plano de carreira, para não acontecer de lá na frente vocês se depararem com um direito que faziam jus mas não usufruíram por desconhecimento.”
    Domingos também frisou a responsabilidade que o cargo traz, pontuando questões de salário e incentivando os estudantes a participarem de seleções. “A profissão é extraordinária,
    é bonita. Vocês têm que ter a visão de que ser professor é promover a mobilidade social. Não podem pensar somente na remuneração. Vá, participe de um edital, experimente, assuma,
    mesmo que seja para lecionar apenas um dia”.
    Após o bate-papo, o público foi convidado para conhecer os laboratórios de Matemática e Física do campus e depois seguiram para as oficinas em que estavam inscritos.
    Alexandre Masaaki Hori cursa Matemática no campus e contou que sempre está presente nas atividades oferecidas pela instituição, pois "é uma oportunidade de aprendizado, 
    de agregar conhecimento, sobretudo as oficinas". O estudante da UEMG, Jair Borges, também viu nas oficinas e palestras a oportunidade de aprendizados direcionados à disciplina.
    “Ainda não havia tido a oportunidade de vir ao campus. Quando os professores divulgaram o evento, logo ficamos curiosos e interessados em participar. Além disso, para o currículo
    é muito válido a questão das horas, das atividades extraclasse”.
    Para o coordenador do curso de Matemática do campus, professor Benjamin Esteves, o evento foi satisfatório e os estudantes se envolveram bastante nas atividades 
    “Foi produtivo, eles se sentiram motivados, pois houve um espaço para eles proporem suas próprias oficinas, muitas delas baseadas em projetos de pesquisa e 
  extensão dos quais já participavam”.</p>
  
