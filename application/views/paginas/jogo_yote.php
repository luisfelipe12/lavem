			<h4 class="card-title"><b>Objetivos do jogo</b></h4>
			<p>- Desenvolver habilidades necessárias ao resgate da cultura africana (jogo original) ou de outras culturas que podem ser utilizadas na adaptação do jogo;</p>
			<p>- Aprimorar ou desenvolver o pensamento estratégico durante as jogadas;</p>
			<p>- Trabalhar uma das principais tendências em educação matemática: a etnomatemática;</p>
			<p>- Trabalhar as quatro operações com expressões numéricas nesta adaptação.</p>
			
			
			<h4 class="card-title"><b>Organização da classe/ Anos de Aplicação</b></h4>
			<p>- Do 6° à 9° ano do Ensino Fundamental II e para o Ensino Médio. </p>
			<p>- Grupos de quatro alunos, sendo dupla contra dupla.</p>

			<h4 class="card-title"><b>Regras modificadas</b></h4>
			<p><b>Participantes:</b> O yoté é um jogo para dois jogadores, mas pode também ser praticado em grupo de quatro pessoas (dupla contra dupla). </p>
			<p><b>Preparação:</b> Cada jogador ou jogadora terá ao seu dispor, 12 peças e 17 fichas contendo com expressões numéricas e em seus versos datas nacionais comemorativas, que estarão fora do tabuleiro no início do jogo.</p>
			<p><b>Como se joga:</b> Inicialmente faz-se um sorteio para definir quem iniciará o jogo. Cada jogador coloca uma peça no tabuleiro na posição que desejar. A partir da primeira jogada os jogadores podem optar por colocar uma nova peça ou movimentar as peças que já estão no tabuleiro.</p>
			<p><b>Movimento das peças no tabuleiro:</b> As peças podem ser movimentas para cima, para baixo, para esquerda ou para direita, sempre “caminhando” somente uma casa e as fichas ficarão dispostas na mesa com as datas comemorativas voltadas para cima. As peças não podem ser movimentadas na diagonal. </p>
			<p><b>Captura das peças:</b> A captura será feita no mesmo sentido do movimento, saltando a peça adversária e caindo imediatamente na casa vaga após a peça que será capturada, porém só ocorrerá esta captura, se o participante responder corretamente a data e expressão de uma das fichas disposta na mesa.</p> 
			<p><b>Vencedor:</b> Será vencedor quem capturar todas as peças adversárias ou bloquear as peças adversárias restantes. Se os dois jogadores ou jogadoras ficarem com as peças bloqueadas (sem condições de movimento) será vencedor quem tiver mais peças no tabuleiro.
			
			<h4 class="card-title"><b>Algumas problematizações</b></h4>
			
			<p>A problematização está dentro do jogo que foi modificado, porém após o termino da rodada o professor poderá adotar as expressões que os alunos tiveram dificuldade para fazer um trabalhar a partir delas, assim como sugerir que os alunos escrevam seus próprios problemas tendo as mesmas como base.</p>
			<p>Com os resultados anotados na folha poderemos pedir para que os alunos façam a Média Aritmética Simples ou a Média Aritmética Ponderada, atribuindo peso a cada resultado da jogada.</p>
			
