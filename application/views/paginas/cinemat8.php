     <p>Já parou para pensar como tudo começa pelo número 1 e que nossa vida é movida por ele? Mas de onde será que surgiu essa ideia?
      O documentário, "A História do Número 1", realiza um passeio pela história da matemática tendo como personagem principal o número um. Este representa o início de tudo
      e possui os primeiros registros históricos de sua representação grafados em ossos para exprimir quantidades em uma sucessão de traços que permitiam a contagem. 
      Analisando os sumérios, o documentário atribui à sua notação de representação do número um em cones de argila como a responsável por possibilitar a representação 
      da subtração e, assim, dar origem à aritmética. 
      Sobre os algarismos hindu-arábicos, o documentário defende que seria mais correto denominá-los indianos,
      pois esses povos já utilizavam esse sistema algorítmico milhares de anos antes de Cristo, e os árabes,
      nesse processo, foram responsáveis por levá-los à Europa. Esses algarismos traziam uma novidade revolucionária: 
      o número zero, o qual passa a dividir as atenções com o personagem principal do documentário.
      Como a representação do nada foi recebida pela sociedade europeia, e porque o uso do zero revolucionou
      a representação tanto de grandes quantidades quanto de muito pequenas são questões trabalhadas neste filme.
      Além disso, a obra analisa como os números um e zero se tornaram os responsáveis por uma das mais importantes 
    revoluções do conhecimento humano: a informatização.</p>
