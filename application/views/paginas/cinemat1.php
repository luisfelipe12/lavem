     <p>"Donald no país da Matemágica" foi lançado nos E.U.A. em 1959. Tornou-se um dos mais populares filmes educativos já produzidos pela Disney. 
      Foi indicado ao Óscar de melhor curta documentário de 1959.
      O filme conta a história de um personagem, o “Pato Donald”, que descobre que entrou em um lugar desconhecido chamado "País da Matemágica”.
      De início o personagem não fica interessado pelo lugar, dizendo que a matemática é para "intelectuais".
      No entanto, ao ser convidado a explorar melhor este lugar desconhecido, o personagem aceita o desafio como uma aventura e 
      passa então a descobrir inúmeras aplicações da matemática na arte, música, arquitetura, corpo humano e na natureza. 
      No desenrolar da história, estas descobertas acabam sendo importantes para mudar a percepção do personagem em relação à 
    forma como ele concebia a Matemática. No final Donald passa a compreender e reconhecer o valor da matemática.</p>
    <p><b>Assuntos de matemática tratados durante o filme:</b></br>
    - A matemática na Grécia: Pitágoras e a música;</br>
  - O Pentagrama, a regra de ouro, e o retângulo de ouro;</br>
- Matemática, arquitetura e arte;</br>
- O corpo humano e a natureza;</br>
- A matemática nos jogos de tabuleiro e bilhar;</br>
- O infinito e futuro.
