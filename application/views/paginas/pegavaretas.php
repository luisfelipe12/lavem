			<p>(2) Organizar a pontuação conquistada pelos participantes do grupo em uma tabela e posteriormente em um gráfico de barras.
			</p>
			<p>(3) Organizar todos os gráficos de barras construídos a partir da pontuação conquistada no jogo em um painel comparativo definindo qual foi o ganhador da sala.
			</p>
