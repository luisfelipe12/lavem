			<p>Duplas ou grupos com quatro alunos.</p>
			
			<h4 class="card-title"><b>Anos de aplicação</b></h4>
			<p>Anos iniciais, intermediários ou finais do ensino fundamental e primeiro ano do ensino médio.</p>
			
			<h4 class="card-title"><b>Regras do jogo</b></h4>
			<p>O jogo pega varetas é composto por varetas coloridas: vermelhas, amarelas, verdes,
				azuis e preta. A cada cor é atribuído um valor: (a) Vermelha: 10 pontos; (b) Amarela: 1 ponto;
			(c) Verde: 2 pontos; (d) Azul: 5 pontos e (e) Preta: 25 pontos.</p>
			
			<p>Os jogadores organizados em duplas ou grupos com quatro participantes devem eleger um único jogador para segurar as varetas e solta-las sobre o centro de uma mesa. Os participantes irão seguir o sentido anti-horário em relação a esse jogador durante todo o jogo. Feito isso, cada jogador irá retirar, na sua vez, uma das varetas sem mexer as demais. Caso consiga, poderá retirar outra, senão passará a vez da jogada para outro participante, recolocando a vareta no jogo. Pode-se retirar as varetas utilizando outras varetas já retiradas como apoio. Vence o jogo o participante que somar mais pontos em relação ao número de varetas conquistadas.
			</p>
			
			
			<h4 class="card-title"><b>Orientação aos alunos quanto às regras</b></h4>
			
			<p>1. Cada jogador, após colocar seu nome em uma folha de papel sulfite a organiza para
			anotações pessoais:</p>
			
			
			
			<p>2. Após se organizarem em grupo, decide-se quem irá soltar as varetas sobre o centro da
			mesa.</p>
			
			<p>3. Seguindo as regras do jogo, inicia-se a partida no sentido anti-horário, pelo
			participante à direita do jogador que soltou as varetas no centro da mesa.</p>
			
			
			<p>4. Na sua vez, o jogador tenta retirar uma vareta da cor que lhe parece mais conveniente. Se ele retirou uma vareta vermelha, por exemplo, registra nas suas anotações pessoais a cor e o valor correspondente a vareta conquistada e volta a retirar uma nova vareta enquanto consiga efetuar a retirada de maneira correta. O jogador passará a vez se ao retirar uma vareta e as demais mexerem.
			</p>
			
			
			<p>5. No final da partida, os jogadores somarão os pontos registrados nas anotações
			pessoais realizando as comparações necessárias e definindo o(s) ganhador(es).</p>
			
			<h4 class="card-title"><b>Algumas problematizações</b></h4>
			
			<p>O jogo pega varetas como estratégia de ensino ou consolidação de habilidades relacionadas à
