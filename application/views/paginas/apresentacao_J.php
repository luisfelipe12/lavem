    <h1 class="mt-4 mb-3" style="text-align:center;">Jogos e suas Problematizações no Ensino de Matemática</h1>

    <!-- Intro Content -->
    <div class="row">
     <div class="col-lg-12" style="text-align: justify;">
      <p>A utilização de jogos nas aulas de matemática contribui para a criação de um contexto que permite ao aluno desenvolver competências e habilidades cognitivas essenciais à resolução de problemas, tais como: elaborar e rever estratégias de solução, monitorar todo o processo de resolução, tomar decisões a partir das ações de seu oponente, fazer antecipações, entre outras.
      </p>
      <p>Os jogos nas aulas de matemática caracterizam-se como recursos metodológicos promissores quando problematizados. Segundo Smole, Diniz e Cândido (2000), a resolução de problemas pode ser entendida como uma possibilidade do docente estimular os alunos afim de que os mesmos sintam-se capazes de vencer suas dificuldades, e percebam seu progresso:
      </p>
      <p style="margin-left:70px;">Um dos maiores motivos para o estudo da matemática é desenvolver a
        habilidade de resolver problemas. Essa habilidade é importante não
        apenas para aprendizagem em matemática da criança, mas também
        para o desenvolvimento de suas potencialidades em termos de
        inteligência e cognição. Por isso, acreditamos que a resolução de
        problemas deve estar presente no ensino de matemática, em todas as
        séries escolares, não só pela sua importância como forma de
        desenvolver várias habilidades, mas especialmente por possibilitar ao
        aluno a alegria de vencer obstáculos criados por sua própria
        curiosidade, vivenciando, assim, o que significa fazer matemática.
      (SMOLE, DINIZ e CÂNDIDO, 2000, pág. 13).</p>
      <p>O prazer inigualável de vencer desafios na Resolução de Problemas e a necessidade de traçar e rever estratégias de jogo para uma tomada de decisão mais assertiva na superação de obstáculos constitui habilidades no aluno que serão úteis para toda sua vida.
      </p>
      <p>Para Vygotsky (1988) o jogo coloca a criança diante de um desafio promissor quanto à aprendizagem de conceitos:
      </p>
      <p style="margin-left:70px;">No jogo a criança é sempre superior à sua média de idade, superior ao
        seu comportamento usual de todo dia; no jogo ela está como que à
        frente de si mesma. O jogo contém de modo condensado, como no
        foco de uma lente de aumento, todas as tendências do
        desenvolvimento; no jogo a criança tenta como que completar um
        salto sobre o nível de seu comportamento ordinário. A relação do jogo
        com o desenvolvimento poderia ser comparada com a do ensino-
        aprendizagem com o desenvolvimento. As mudanças de necessidades
        e a consciência de um tipo mais geral situam-se por detrás do jogo. O
        jogo é o recurso do desenvolvimento e cria a zona do
        desenvolvimento mais próximo. A ação no campo imaginário, na
        situação imaginada, a construção da intenção voluntária, a formação
        do plano de vida, motivos da vontade – tudo isso emerge no jogo e...
      (VYGOTSKY, 1988, pág. 74-75).</p>
      <p>À luz da teoria sócio-histórica o contexto de problematização dos jogos matemáticos permitirá ao professor lançar um olhar para a estrutura cognitiva do estudante, possibilitando uma sondagem quanto aos conhecimentos espontâneos preexistentes que serão úteis para a formação de conceitos científicos (escolares), por meio da mediação.
      </p>
      <p>Quando articulados a outros materiais de apoio curricular, a problematização dos jogos matemáticos permitirá uma maior flexibilidade metodológica ao professor possibilitando realizar sondagens e intervenções pontuais quanto à formação de noções ou conceitos almejados.
      </p>
