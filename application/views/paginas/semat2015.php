			ela falou sobre as diferentes técnicas de demonstração e produção do conhecimento na área da Matemática Pura.</p>
			<p style="text-align:justify;">6º dia do evento: Uso da tecnologia portátil no ensino de Matemática
				Para fechar o último dia de evento na tarde do último sábado, os professores Fredy Coelho Rodrigues e Benjamim José Esteves realizaram um minicurso sobre o uso da
				calculadora científica. Os acadêmicos tiveram a oportunidade de discutir o uso dessa tecnologia no ensino bem como a utilização de uma sequência didática para mediar
