				<h4 class="card-title"><b>Materiais Necessários</b></h4>
				<p>- Pequenos saquinhos com arroz ou outro objeto, marcados de um lado com números e do outro, com figuras geométricas;</p>
				<p>- Folha Modelo com as figuras geométricas;</p>
				<p>- Caneta, lápis, tesoura, borracha.</p>
			
			<h4 class="card-title"><b>Objetivos do Jogo</b></h4>
			<p>- Trabalhar a concentração e a coordenação motora;</p>
			<p>- Reconhecer as figuras planas e calcular os valores de suas áreas;</p>
			<p>- Resgatar e consolidar alguns conceitos matemáticos importantes utilizados no cálculo da área das figuras;</p>
			<p>- Estimular o cálculo mental e a criação de estratégias;</p>
			<p>- Fazer com que os alunos façam suas próprias descobertas durante o jogo;</p>
			<p>- Trabalhar jogos na perspectiva da “Etnomatemática”.</p>

			
			
	
		
			
			<h4 class="card-title"><b>Organização da Classe</b></h4>
			<p>- Grupos de 3 a 6 alunos.</p>
			
			
			<h4 class="card-title"><b>Ano de Aplicação</b></h4>
			<p>- 8º Ano do Ensino Fundamental II.</p>

			<h4 class="card-title"><b>Informações sobre o Jogo</b></h4>
			<p>- Cada grupo receberá 13 peças (saquinhos), sendo 12 marcadas com figuras geométricas e números e uma “neutra” (N);</p>
			<p>- Cada integrante do grupo contará com uma folha modelo (Exemplificada a seguir).</p>

			
			
			
			<h4 class="card-title"><b>Orientação aos alunos quanto às regras</b></h4>
			<p><b>1º - Quem começa?</b></p>
			<p>Em uma mesa plana, dispor as peças (saquinhos) de forma que a parte numérica esteja voltada para baixo;</p>
			<p>Misturar as peças e cada jogador deve pegar uma. Começa o jogador que tirar o maior número na primeira retirada. O sentido que o jogo irá tomar depende dos dois outros jogadores, que estão à direita ou a esquerda do jogador, aquele que retirou o segundo número maior dará continuidade ao jogo.</p>

			<p><b>2º - Desenvolvimento do Jogo</b></p> 
			<p>- Após ter definido quem começa a rodada, dispor as peças de forma que a parte geométrica esteja voltada para baixo;</p>
			<p><b>* A primeira rodada será exclusiva para definir quais figuras o jogador irá efetuar o cálculo da área.</b></p>
			<p>- Para iniciar, pegue a peça neutra com uma das mãos e arremesse para cima com o objetivo de pegar outras duas peças que estão sobre a mesa;</p>
			<p>- Marque na folha modelo o par de figuras que corresponde ao desenho nas peças. Devolva as peças a mesa e misture, os próximos jogadores irão fazer o mesmo processo;</p>
			<p>- Assim que todos escolherem as suas respectivas figuras, irá encontrar na folha modelo uma tabela indicando uma expressão algébrica correspondente a figura. Verifique e identifique em qual fórmula deverá ser substituída a expressão;</p>

