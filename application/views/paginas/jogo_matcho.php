			</div>			
			
			
			
			
			<p><b>* A segunda rodada será exclusiva para definir os valores de “X” que serão atribuídos a cada expressão gerada.</b></p>
			<p>- Dispor as peças de forma que a parte numérica esteja voltada para baixo;</p>
			<p>- O jogador que iniciará a segunda rodada será o mesmo que começou o jogo. Ele irá realizar o procedimento de pegar a peça neutra e arremessar sem deixar cair, só que desta vez, ele pode pegar quantas peças ele conseguir, porém dentre as peças, ele terá que escolher qual valor se encaixa melhor nas expressões geradas de modo que consiga um maior valor de área.</p>
			<p>- Os demais jogadores, realizarão o mesmo procedimento.</p>
			<p>OBS: Quando o jogador deixar cair a peça neutra, ele terá somente a opção de escolher rapidamente uma peça, não tendo as opções de escolhas que teria o jogador que conseguiu conquistá-la.</p>
			<h4 class="card-title"><b>3º - Quem ganha?</b></h4>

	  
			<p>- Ganha a partida o jogador que possuir o maior valor de área (m²). Para isso, ele deve somar as duas áreas calculadas e comparar com os demais participantes.</p>
			<p>Caso houver empate nos valores, o desempate será de acordo com a seguinte ordem:</p>
			<p>1º- Maior área círculo;</p>
			<p>2º- Maior área triângulo</p>
			<p>3º- Maior área trapézio;</p>
			<p>4º- Maior área quadrado;</p>
			<p>5º- Maior área losango;</p>
			<p>6º- Maior área retângulo.</p>

			
			
			
			
